# Verbalmente
# Ejemplo: “El precio aumento en 2 dólares por cada kilómetro recorrido”

# Numéricamente
# Puede ser con una tabla de valores donde tenemos un valor x y un valor f(x) que le corresponde

# Visualmente
# La gráfica en un plano cartesiano, una forma muy común de comprobar si es una función de esta manera,
# es trazar una línea sobre la función y si pasa a tocar más de 1 punto, entonces no es una función.

