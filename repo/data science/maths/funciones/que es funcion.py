# Tipos de variables

# Variables cualitativas

# Variables nominales: Se les asigna una cualidad (rojo, verde, azul, etc. en el caso de un LED)

# Variables ordinales: Representan un orden. Por ejemplo, la altura de un objetos se puede clasificar
# como alto, medio o bajo

# Variables binarias: Solo toman 2 valores, usualmente usadas para representar estados.
# (frío o caliente, uno o cero)


# Variables cuantitativas

# Variables discretas: Representan un grupo de datos que son finitos y toman ciertos valores.
# Pueden verse como variables separadas por un “paso”. Por ejemplo, una persona puede tener 0,1,2,3 amigos
# pero no 3.5 amigos

# Variables continuas: Sus valores pueden verse como infinitos al tomar
# cualquier valor dentro de los números reales en un rango establecido.
# Por ejemplo, la altura de una persona
# Lo que se suele modelar para escribir funciones, se suele encontrar en
# el grupo de variables cuantitativas.

